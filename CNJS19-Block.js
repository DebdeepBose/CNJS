/*

A block in JavaScript is simply a group of statements wrapped in curly braces { }.

{                      
    let x = 2;           
    console.log(x);      
}                      

In fact,

{    
    This is also a perfectly valid JS code on its own, which is a block
}

A block creates a block scope for variables declared with let and const.
A block is also called a Compound Statement.
A block helps us group multiple statements together where JS expects 1 statement.

*/
